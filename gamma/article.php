		<h2 class="article-title">To Settle Mars</h2>
		<h3 class="article-author"><a href="">Frank Stratford</a></h3>
		<h4 class="article-date">August 5, 2005</h3>
		<p>Anyone who knows me knows that my main topic of interest lies in ways of gaining the funding we need for future missions, settlement and terraforming of Mars. It is a vital subject and without it there can be no future on Mars. I have been of the view that there is enough colonization and terraforming information out there so why focus on the end result when we can't even deal with the funding problems? But the simple fact is, based on pure science we have very few tested plans for the first mission and settlement of Mars. There are many theories but little actual hard data to go on, or so it seems. In this article I want to discuss some of the ideas and requirements for a successful settlement on Mars, especially in those first years after landing.</p>
		<p>Getting to Mars will be a challenge in itself but as many in the Mars advocacy community have pointed out, we do have the technology right now to launch such a mission. Some of the launch vehicles mentioned could be- The Russian Energia is described as "the most powerful booster in the world" and could easily be used in a successful Mars mission. At first glance, it looks refreshingly similar to those familiar with the US Space Shuttle. That initial impression is where the similarities end, however. The Energia configuration consists of a large central core similar in appearance to the Space Shuttle's external tank. One important difference, however, is that this is a genuine rocket stage complete with four engines (comparable to the Space Shuttle Main Engines) and guidance system<a class="footnote" href="">[1]</a>. Four booster stages are arranged in two pairs on either side of the central core. Although these look similar to the Space Shuttle's solid rocket boosters, they are, in fact, liquid-fuelled. In other applications these rockets actually form the first stage of another Russian launch vehicle called 'Zenit'. One interesting feature of the Energia rocket is the location of the payload. Rather than mounted axially on the top of the rocket, Energia mounts the payload on its back. In 1987 the Energia lifted the heaviest payload ever of 100,000 kg. A manned mission to Mars could easily be made to fit this requirement. <img src="_images/structure/yin-yang-end.gif" alt="end"></p>
